package edunova08;

public class Racunalo {

	private String ime;
	private double cijena;
	private boolean ukraden;
	private int cijenaMisa;
	private float herci;

	public String getIme() {
		return ime;
	}

	public void setIme(String ime) {
		this.ime = ime;
	}

	public double getCijena() {
		return cijena;
	}

	public void setCijena(double cijena) {
		this.cijena = cijena;
	}

	public boolean isUkraden() {
		return ukraden;
	}

	public void setUkraden(boolean ukraden) {
		this.ukraden = ukraden;
	}

	public int getCijenaMisa() {
		return cijenaMisa;
	}

	public void setCijenaMisa(int cijenaMisa) {
		this.cijenaMisa = cijenaMisa;
	}

	public float getHerci() {
		return herci;
	}

	public void setHerci(float herci) {
		this.herci = herci;
	}

}
