    private void napuniModel() {
      var s=obrada.getEntitet();
      s.setNaziv(txtNaziv.getText());
        try {
          s.setCijena(BigDecimal.valueOf(df.parse(txtCijena.getText()).doubleValue()));
        } catch (Exception e) {
            s.setCijena(BigDecimal.ZERO);
        }
        
         try {
          s.setNetoKolicina(BigDecimal.valueOf(df.parse(txtNetoKoliƒçina.getText()).doubleValue()));
        } catch (Exception e) {
            s.setNetoKolicina(BigDecimal.ZERO);
        }
        
        
    }

    private void napuniView() {
      var s= obrada.getEntitet();
      txtNaziv.setText(s.getNaziv());
      txtCijena.setText(df.format(s.getCijena()));
      txtNetoKoliƒçina.setText(df.format(s.getNetoKolicina()));
      
    }
